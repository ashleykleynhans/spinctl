func TestExportConfigsCreatesSpinnakerYml(t *testing.T) {
	cfg := config.NewDefault()
	cfg.Version = "2025.3.2"
	cfg.Timezone = "America/Los_Angeles"
	gate := cfg.Services[model.Gate]
	gate.Enabled = true
	cfg.Services[model.Gate] = gate

	dir := t.TempDir()
	if err := ExportConfigs(cfg, dir); err != nil {
		t.Fatalf("ExportConfigs: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(dir, "spinnaker.yml"))
	if err != nil {
		t.Fatalf("reading spinnaker.yml: %v", err)
	}
	content := string(data)
	if !strings.Contains(content, "gate") {
		t.Error("spinnaker.yml should contain gate service")
	}
	if !strings.Contains(content, "redis") {
		t.Error("spinnaker.yml should contain redis")
	}
	if !strings.Contains(content, "America/Los_Angeles") {
		t.Error("spinnaker.yml should contain timezone")
	}
	if !strings.Contains(content, "autogenerated by spinctl") {
		t.Error("spinnaker.yml should contain header")
	}
}

