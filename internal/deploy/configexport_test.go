func TestExportConfigsLocalYml(t *testing.T) {
	cfg := config.NewDefault()
	cfg.Version = "2025.3.2"
	gate := cfg.Services[model.Gate]
	gate.Enabled = true

	// Add settings via yaml.Node.
	settingsYaml := `server:
  ssl:
    enabled: true
    keyStore: /path/to/keystore.jks
spring:
  security:
    oauth2:
      client:
        registration:
          github:
            client-id: test123`
	var node yaml.Node
	yaml.Unmarshal([]byte(settingsYaml), &node)
	if node.Kind == yaml.DocumentNode && len(node.Content) > 0 {
		gate.Settings = *node.Content[0]
	}
	cfg.Services[model.Gate] = gate

	dir := t.TempDir()
	if err := ExportConfigs(cfg, dir); err != nil {
		t.Fatalf("ExportConfigs: %v", err)
	}

	data, err := os.ReadFile(filepath.Join(dir, "gate-local.yml"))
	if err != nil {
		t.Fatalf("reading gate-local.yml: %v", err)
	}
	content := string(data)
	if !strings.Contains(content, "ssl") {
		t.Error("gate-local.yml should contain ssl settings")
	}
	if !strings.Contains(content, "oauth2") {
		t.Error("gate-local.yml should contain oauth2 settings")
	}
	if !strings.Contains(content, "test123") {
		t.Error("gate-local.yml should contain client-id")
	}
}

